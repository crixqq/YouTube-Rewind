const SUPPORTED_LOCALES = Object.keys(translations);

let currentLocale = 'en';

export function setLocale(lang: string): void {
  if (lang === 'auto') {
    const uiLang = browser.i18n.getUILanguage().toLowerCase();
    const match = SUPPORTED_LOCALES.find(
      (l) => uiLang.startsWith(l) || uiLang.startsWith(l.split('-')[0])
    );
    currentLocale = match || 'en';
  } else {
    currentLocale = lang;
  }
}

export function getLocale(): string {
  return currentLocale;
}

export function t(key: string): string {
  return translations[currentLocale]?.[key] || translations.en?.[key] || key;
}

/** Returns all translations for a key across all languages (for cross-language search). */
export function allTranslations(key: string): string[] {
  const result: string[] = [];
  for (const lang of Object.values(translations)) {
    if (lang[key]) result.push(lang[key].toLowerCase());
  }
  return result;
}

setLocale('auto');
